
/**
 * Represents an unknown event.
 * @interface
 */
export interface UnknownEventInterface {
	/**
	 * The code associated with the unknown event.
	 */
	code: string;

	/**
	 * The message associated with the unknown event.
	 */
	message: string;

	/**
	 * The title of the unknown event.
	 */
	title: string;
}
