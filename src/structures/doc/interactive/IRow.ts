/**
 * Section Row interface
 * @interface
 * @export
 */
export interface Row {
	/**
	 * unique identifier of the row in a section
	 * @memberof Row
	 */
	id: string

	/**
	 * title of the row
	 * @memberof Row
	 */
	title: string

	/**
	 * description of the row
	 * @memberof Row
	 */
	description: string
}
